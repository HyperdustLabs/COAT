"""``opencoat demo`` — one-line guided tour of the OpenCOAT pickup loop.

This is the "zero Python paste" path the ``opencoat-skill`` quickstart
points users at: instead of asking them to write a ~40-line
``demo_host.py``, the CLI wires a tiny in-script ``FakeHost``,
subscribes :func:`install_hooks`, fires three demo events, and prints
**BEFORE / AFTER** for each scene so concerns visibly change the
host's mutable state on first read.

Two modes:

* **default** (daemon-backed) — connects to the running daemon (the
  one ``opencoat runtime up`` exposes). Concerns are expected to be
  seeded via ``opencoat concern import --demo``; the daemon owns
  them and the DCN. Activations show up in
  ``opencoat dcn activation-log`` afterwards.
* **``--in-proc``** — no daemon required. Builds an in-process
  :class:`OpenCOATRuntime`, seeds :func:`demo_concerns` directly,
  runs the same three scenes against it. Useful for a cold tour
  where the user hasn't run ``runtime up`` yet.

``--script-out PATH`` writes the equivalent ``demo_host.py`` to disk
and exits without running anything — handy for users who want a
runnable template to adapt for their own host.

The CLI lives in ``opencoat-runtime`` but the demo logic relies on
``opencoat-runtime-host`` for :class:`OpenClawAdapter` /
:func:`install_hooks` (the pickup API). We **lazy import** that
package and fail with a friendly hint if it's missing, so
``pipx install opencoat-runtime`` users get a clear "install the host
SDK too" message rather than an opaque ``ModuleNotFoundError``.
"""

